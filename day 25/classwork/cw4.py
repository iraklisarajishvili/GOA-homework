# 4)შექმენით ფუნქცია სახელად average(), რომელსაც გადაეცემა ინტეჯერების სია, შენ უნდა დააბრუნო ამ სიაში არსებული ინტეჯერების საშუალო არითმეტიკული (საშუალო არითმეტიკული გამოითვლება რიცხვების ჯამი/რიცხვების რაოდენობა)


def average(arr):
    total = 0
    count = 0
    
    for i in arr:
        total += i
        count += 1
        
    return total / count

print(average([1,2,3]))
print(average([10,20,30,40]))